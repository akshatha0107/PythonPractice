name = input("What is your name :")
age = int(input("How old are you? :"))
year = str((2021-age)+100)
print(name + " will be 100 years old in " + year)
number = int(input("How many times do you want to print the message"))
n=1
while(n<=number):
  print(name + " will be 100 years old in  " + year)
  n = n+1